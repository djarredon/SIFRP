public class Holding {
    private int cost;

    public Holding() {
        cost = 0;
    }

    public Holding(int cost) {
        this.cost = cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    public int getCost() {
        return cost;
    }
}
